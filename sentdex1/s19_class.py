#   19. Classes

class calculator:

    def add(x,y):
        result = x+y
        print(result)

    def sub(x,y):
        result = x-y
        print(result)

    def mult(x,y):
        result = x*y
        print(result)

    def div(x,y):
        result = x/y
        print(result)

calculator.mult(3,5)
calculator.sub(5,13)
calculator.div(1,3)
